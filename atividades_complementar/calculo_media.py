# Peça para o usuario informar dois valores e faça a média entre esses valores 


entrada_valor_um = int(input("Digite um valor: "))
entrada_valor_dois = int(input("Digite um valor: "))

media = (entrada_valor_um + entrada_valor_dois) /2

print(f"a média entre {entrada_valor_um} e {entrada_valor_dois} tem como resultado: {media}")


