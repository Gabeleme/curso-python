# receba dois números e faça a divisão entre eles, retorne o valor da divisão e o resto dela

# Recebendo dois números do usuário
numero1 = float(input("Informe o primeiro número: "))
numero2 = float(input("Informe o segundo número: "))

# Calculando a divisão inteira e o resto
divisao_inteira = numero1 // numero2
resto = numero1 % numero2

# Exibindo o resultado
print(f'A divisão entre os números tem como resultado {divisao_inteira}')
print(f'O resto da divisão é {resto}')


















